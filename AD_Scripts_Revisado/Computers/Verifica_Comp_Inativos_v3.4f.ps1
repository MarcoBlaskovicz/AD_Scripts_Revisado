<#
.SYNOPSIS
    Identifies inactive workstations in Active Directory and optionally
    disables them, moves them to a quarantine OU, or undoes those actions via rollback.

.DESCRIPTION
    The script scans an AD domain querying all "computer" objects
    that are NOT servers. For each workstation found, it determines the
    last logon date using two complementary attributes:

      - LastLogonDate        : replicated attribute between DCs (up to 14 days precision).
      - LastLogonTimestamp   : raw value in FileTime format, used as fallback
                               when LastLogonDate is absent.

    Computers whose last logon is before the cutoff (today - $Days days), or
    that have never logged on, are classified as inactive.

    PROTECTION AGAINST ACCIDENTAL SERVER INCLUSION:
      The script applies three independent protection layers:
        1. LDAP Filter  : excludes objects with "Server" in the operatingSystem attribute.
        2. Regex in loop: discards objects that escaped the LDAP filter but still
                          have "Server" in operatingSystem.
        3. -ExcludeServerOU: excludes all objects whose DN belongs to the specified OUs
                          or any nested sub-OU, regardless of the operatingSystem value
                          (covers empty/atypical fields).

    READ-ONLY MODE (default):
      Generates only the inventory CSV with the list of inactive workstations. No
      changes are made to AD. The CSV includes AccountEnabled and
      AlreadyInTargetOU fields to facilitate manual analyst triage.

    REMEDIATION MODE (-Remediate):
      In addition to the inventory, disables each inactive account and moves it to the
      OU specified in -TargetOU. Each action is recorded in a separate audit CSV.
      Supports -WhatIf and -Confirm for safe simulation.
      WARNING: use the AccountEnabled=False and AlreadyInTargetOU=True fields in the
      inventory to identify already-processed accounts before running remediation.

    ROLLBACK MODE (-Rollback):
      Undoes a previous remediation by reading the actions CSV generated by -Remediate
      (provided in -RollbackCsv). For each entry where Disabled=True:
        - Re-enables the account (Enable-ADAccount).
        - Attempts to move the object back to the original OU (extracted from the
          DistinguishedName recorded in the CSV).
        - If the original OU no longer exists in AD, re-enables the account and logs
          a warning — the account is NOT left disabled due to a move failure.
      Only entries where Disabled=True are processed (entries where the
      disable already failed during remediation are ignored).
      Supports -WhatIf and -Confirm for safe simulation.

    INVENTORY CSV FIELDS:
      - Domain             : FQDN of the queried domain.
      - Name               : NetBIOS computer name.
      - OperatingSystem    : operating system registered in AD.
      - LastLogonDate      : date of the last identified logon (may be $null).
      - InactiveDaysApprox : approximate days since last logon.
      - AccountEnabled     : True if the account is active in AD, False if disabled.
      - AlreadyInTargetOU  : True if the object is already in -TargetOU or a sub-OU of it;
                             False if it is in another OU; empty if -TargetOU was not
                             provided. Useful for identifying already-quarantined accounts.
      - DistinguishedName  : full DN of the object in AD.

    COMBINED INTERPRETATION OF AccountEnabled + AlreadyInTargetOU:
      False + True  -> Already properly handled. No action needed.
      True  + True  -> In quarantine but still active. Review manually.
      False + False -> Disabled but outside quarantine. Review manually.
      True  + False -> Primary candidate for remediation.

    GENERATED OUTPUTS (in C:\Temp\LogScripts\AD_Inactive_Workstations\<domain>_<timestamp>\):
      - Execution_<timestamp>.log                             : line-by-line execution log.
      - Workstations_Inactive_<domain>_<days>days.csv        : inactive workstations inventory
                                                               with all fields described above.
      - Workstations_Inactive_Actions_<timestamp>.csv        : record of all remediation actions
                                                               (only with -Remediate).
      - Workstations_Remediation_Failures_<timestamp>.csv    : computers where the disable
                                                               or move operation failed (only with
                                                               -Remediate, when there are errors).
      - Workstations_Rollback_Actions_<timestamp>.csv        : record of each rollback action
                                                               (only with -Rollback).
      - Workstations_Rollback_Failures_<timestamp>.csv       : computers where rollback failed
                                                               (only with -Rollback, when there are errors).

.PARAMETER Domain
    FQDN of the domain to scan (e.g.: corp.contoso.com).
    If omitted, the script prompts interactively via Read-Host.

.PARAMETER Days
    Number of days without logon to consider a workstation inactive.
    Default: 60 days.

.PARAMETER Remediate
    Switch. When present, disables and moves each inactive workstation to -TargetOU.
    Cannot be used together with -Rollback.
    WARNING: destructive action. Use -WhatIf for simulation before running in production.
    It is recommended to review the inventory CSV (AccountEnabled and AlreadyInTargetOU fields)
    before executing to avoid reprocessing already-handled accounts.

.PARAMETER TargetOU
    Distinguished Name (DN) of the target OU where inactive accounts will be moved.
    Required when -Remediate is used. If omitted, the script prompts interactively.
    When provided in read-only mode, the AlreadyInTargetOU field in the CSV indicates
    which computers are already in that OU.
    Example: "OU=Inactive,OU=Workstations,DC=corp,DC=contoso,DC=com"

.PARAMETER Rollback
    Switch. When present, undoes a previous remediation by reading the CSV provided in
    -RollbackCsv. Re-enables accounts and attempts to move them back to the original OU.
    Cannot be used together with -Remediate.
    WARNING: destructive action. Use -WhatIf for simulation before running in production.
    Note: rollback does not restore the previous Enabled state. Accounts that were already
    disabled before remediation will be re-enabled. Refer to the AccountEnabled field
    in the inventory CSV generated before remediation for reference.

.PARAMETER RollbackCsv
    Full path of the actions CSV file generated by a previous run with
    -Remediate (Workstations_Inactive_Actions_<timestamp>.csv).
    Required when -Rollback is used. If omitted, the script prompts interactively.

.PARAMETER ExcludeServerOU
    List of Distinguished Names (DNs) of OUs that contain servers and should be
    completely ignored in the search. Any object whose DistinguishedName contains
    one of the provided DNs will be discarded, including objects in nested sub-OUs at
    any depth level. Each OU is validated in AD before the query — invalid OUs
    generate a warning in the log and are ignored without aborting execution.
    Accepts one or more comma-separated values. Optional.
    Example: -ExcludeServerOU "OU=Servers,DC=corp,DC=contoso,DC=com","OU=Infra,DC=corp,DC=contoso,DC=com"

.PARAMETER Root
    Root path where log/CSV folders will be created. A subfolder per execution
    is created automatically with the pattern <domain>_<timestamp>.
    Default: C:\Temp\LogScripts\AD_Inactive_Workstations

.PARAMETER CsvEncoding
    Encoding for the generated CSV files. Accepted values: utf8, unicode, ascii.
    Note: in PowerShell 5.1, utf8 generates files with BOM. Use unicode if the CSV
    will be consumed by tools that do not tolerate BOM.
    Default: utf8

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1

    Runs interactively: prompts for the domain and generates only the inactive
    workstations inventory (no remediation). The AccountEnabled and AlreadyInTargetOU
    fields will be present in the CSV for triage.

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1 -Domain corp.contoso.com -Days 90 `
        -TargetOU "OU=Inactive,OU=Workstations,DC=corp,DC=contoso,DC=com" `
        -ExcludeServerOU "OU=Servers,DC=corp,DC=contoso,DC=com"

    Inventory of workstations inactive for more than 90 days, with AlreadyInTargetOU
    populated and server OUs excluded from the search.

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1 -Domain corp.contoso.com -Days 60 -Remediate `
        -TargetOU "OU=Inactive,OU=Workstations,DC=corp,DC=contoso,DC=com" `
        -ExcludeServerOU "OU=Servers,DC=corp,DC=contoso,DC=com" -WhatIf

    Simulates remediation excluding the server OU (without changing anything in AD).
    Recommended before the actual execution.

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1 -Domain corp.contoso.com -Days 60 -Remediate `
        -TargetOU "OU=Inactive,OU=Workstations,DC=corp,DC=contoso,DC=com" `
        -ExcludeServerOU "OU=Servers,DC=corp,DC=contoso,DC=com" -Confirm:$false

    Runs remediation without individual per-computer confirmation.

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1 -Domain corp.contoso.com -Rollback `
        -RollbackCsv "C:\Temp\LogScripts\AD_Inactive_Workstations\corp_contoso_com_20260305_143000\Workstations_Inactive_Actions_20260305_143000.csv" -WhatIf

    Simulates the remediation rollback (without changing anything in AD).
    Recommended before the actual execution.

.EXAMPLE
    .\Verifica_Comp_Inativos_v3.4.ps1 -Domain corp.contoso.com -Rollback `
        -RollbackCsv "C:\Temp\...\Workstations_Inactive_Actions_20260305_143000.csv" -Confirm:$false

    Runs rollback without individual per-computer confirmation.

.NOTES
    Requirements:
      - PowerShell 5.1 or later
      - ActiveDirectory module (RSAT: AD DS and AD LDS Tools)
      - Read permission in AD for inventory mode
      - Permission to disable accounts and move objects for remediation/rollback mode

    Recommended usage flow:
      1. Run in read-only mode with -TargetOU and -ExcludeServerOU to
         generate the complete inventory with all triage fields.
      2. Review the CSV — filter by AccountEnabled=True and AlreadyInTargetOU=False
         to identify the actual remediation candidates.
      3. Run with -Remediate -WhatIf to simulate the impact scope.
      4. Run with -Remediate -Confirm:$false to apply the changes.
      5. If needed, run with -Rollback pointing to the actions CSV.

    Security considerations:
      - The script does NOT delete accounts. It only disables and moves them to the quarantine OU.
      - The actions CSV serves as an audit trail for all changes made.
      - It is recommended to keep log files for at least 90 days for auditing.
      - Rollback processes only accounts that were effectively disabled (Disabled=True).
      - If the original OU no longer exists during rollback, the account is re-enabled but not
        moved. The analyst must manually relocate the objects indicated in the CSV.

    Known limitations:
      - LastLogonDate is replicated with up to 14 days latency between DCs. Computers
        with a recent logon on another DC may falsely appear as inactive.
      - The LDAP filter and the regex layer exclude objects with "Server" in the
        operatingSystem attribute, but systems with atypical names or empty fields may not be
        filtered — use -ExcludeServerOU to cover those cases.
      - Rollback does not restore the previous Enabled state of the account. Accounts already
        disabled before remediation will be re-enabled. Refer to the AccountEnabled field
        in the inventory CSV generated before remediation.

