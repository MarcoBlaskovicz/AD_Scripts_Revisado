function Invoke-InactiveWorkstations {
<#
.SYNOPSIS
    Scans an AD domain and identifies/remediates inactive workstations.
.DESCRIPTION
    Queries all computer objects of the workstation type (excludes servers
    by the operatingSystem attribute), evaluates the last logon date of each one
    and classifies as inactive every computer that has not logged on within
    the period defined by -Days. Generates an inventory CSV and, optionally,
    disables and moves the inactive accounts to a quarantine OU.
#>
    [CmdletBinding(SupportsShouldProcess=$true, ConfirmImpact='High')]
    param(
        [Parameter(Position=0, Mandatory=$false)] [string]$Domain,
        [int]$Days = 60,
        [switch]$Remediate,
        [string]$TargetOU,
        [switch]$Rollback,
        [string]$RollbackCsv,
        [string[]]$ExcludeServerOU = @(),
        [string]$Root = 'C:\Temp\LogScripts\AD_Inactive_Workstations',
        [ValidateSet('utf8','unicode','ascii')] [string]$CsvEncoding = 'utf8'
    )

    # ------------------------------------------------------------------
    # PREREQUISITES
    # ------------------------------------------------------------------

    # -Remediate and -Rollback are mutually exclusive
    if ($Remediate -and $Rollback) {
        throw '-Remediate and -Rollback cannot be used simultaneously.'
    }

    # Check and import the ActiveDirectory module (requires RSAT installed)
    if (-not (Get-Module -Name ActiveDirectory)) {
        try { Import-Module ActiveDirectory -ErrorAction Stop } catch { throw "ActiveDirectory module not found." }
    }

    # Prompts for domain interactively if not passed as parameter
    if ([string]::IsNullOrWhiteSpace($Domain)) {
        $Domain = Read-Host 'Enter the domain (FQDN) to scan (e.g.: corp.contoso.com)'
        if ([string]::IsNullOrWhiteSpace($Domain)) { throw 'Domain not provided.' }
    }

    # ------------------------------------------------------------------
    # PATH AND LOG INITIALIZATION
    # ------------------------------------------------------------------

    # Cutoff date: computers without logon before this date are inactive
    $cutoff = (Get-Date).AddDays(-$Days)

    # Replaces dots with underscores for safe use in file/folder names
    $domainTag = ($Domain -replace '\.', '_')

    $runDir              = Initialize-Folders -Root $Root -DomainTag $domainTag
    $runTs               = Get-Date -Format 'yyyyMMdd_HHmmss'
    $logFile             = Join-Path $runDir ("Execution_{0}.log"                              -f $runTs)
    $csvInventory        = Join-Path $runDir ("Workstations_Inactive_{0}_{1}days.csv"          -f $domainTag, $Days)
    $csvActions          = Join-Path $runDir ("Workstations_Inactive_Actions_{0}.csv"          -f $runTs)
    $csvFailed           = Join-Path $runDir ("Workstations_Remediation_Failures_{0}.csv"      -f $runTs)
    $csvRollbackActions  = Join-Path $runDir ("Workstations_Rollback_Actions_{0}.csv"          -f $runTs)
    $csvRollbackFailed   = Join-Path $runDir ("Workstations_Rollback_Failures_{0}.csv"         -f $runTs)

    Write-Log -LogFile $logFile -Level INFO -Message "Start | Domain=$Domain | Days=$Days | Cutoff=$($cutoff.ToString('yyyy-MM-dd')) | Excluded OUs=$($ExcludeServerOU.Count)"

    # ------------------------------------------------------------------
    # ACTIVE DIRECTORY CONNECTION
    # ------------------------------------------------------------------

    # Automatically discovers the nearest DC for the target domain
    try {
        # HostName returns ADPropertyValueCollection — [string] forces safe conversion to string
        $dc         = [string](Get-ADDomainController -Discover -DomainName $Domain).HostName
        $searchBase = (Get-ADDomain -Identity $Domain).DistinguishedName
    } catch {
        Write-Log -LogFile $logFile -Level ERROR -Message "Error connecting to AD: $($_.Exception.Message)"
        throw
    }

    # ------------------------------------------------------------------
    # VALIDATION AND NORMALIZATION OF EXCLUDED OUs
    # ------------------------------------------------------------------

    # Normalizes each DN to lowercase for case-insensitive comparison.
    # Validates the existence of each OU in AD — invalid OUs generate WARN and are ignored
    # (they do not abort execution, as they are an additional protection, not a mandatory block).
    $excludedOUNorm = [System.Collections.Generic.List[string]]::new()
    foreach ($ouDN in $ExcludeServerOU) {
        if ([string]::IsNullOrWhiteSpace($ouDN)) { continue }
        try {
            Get-ADOrganizationalUnit -Identity $ouDN -Server $dc -ErrorAction Stop | Out-Null
            $excludedOUNorm.Add($ouDN.ToLower())
            Write-Log -LogFile $logFile -Level INFO -Message "Validated excluded OU: $ouDN"
        } catch {
            Write-Log -LogFile $logFile -Level WARN -Message "Excluded OU not found in AD (will be ignored): $ouDN | $($_.Exception.Message)"
        }
    }

    # ------------------------------------------------------------------
    # ROLLBACK MODE — executes and exits without querying AD
    # ------------------------------------------------------------------

    if ($Rollback) {
        # Prompts for CSV interactively if not passed as parameter
        if ([string]::IsNullOrWhiteSpace($RollbackCsv)) {
            $RollbackCsv = Read-Host 'Enter the path of the remediation actions CSV (Workstations_Inactive_Actions_*.csv)'
        }
        if ([string]::IsNullOrWhiteSpace($RollbackCsv)) {
            Write-Log -LogFile $logFile -Level ERROR -Message 'Rollback CSV path not provided. Operation cancelled.'
            return
        }
        if (-not (Test-Path $RollbackCsv)) {
            Write-Log -LogFile $logFile -Level ERROR -Message "Rollback CSV not found: $RollbackCsv"
            return
        }

        Write-Log -LogFile $logFile -Level INFO -Message "Rollback start | CSV=$RollbackCsv"

        Invoke-Rollback `
            -CsvPath            $RollbackCsv `
            -Dc                 ($dc | Select-Object -First 1) `
            -LogFile            $logFile `
            -CsvRollbackActions $csvRollbackActions `
            -CsvRollbackFailed  $csvRollbackFailed `
            -CsvEncoding        $CsvEncoding `
            -CallerCmdlet       $PSCmdlet

        return
    }

    # ------------------------------------------------------------------
    # AD QUERY
    # ------------------------------------------------------------------

    # Properties loaded per object — minimizes network traffic
    $props = @(
        'Name','DNSHostName','OperatingSystem','OperatingSystemVersion',
        'LastLogonTimestamp','LastLogonDate',
        'whenCreated','whenChanged','Enabled',
        'DistinguishedName','CanonicalName','PasswordLastSet'
    )

    # LDAP filter: only computer objects that do NOT have "Server" in the operatingSystem field
    # Note: the heuristic in the loop below complements this filter for atypical cases
    $ldap = '(&(objectCategory=computer)(!(operatingSystem=*Server*)))'

    Write-Log -LogFile $logFile -Level INFO -Message "Searching computers in AD..."

    # @() ensures array even if the query returns zero or a single object
    # -ResultSetSize $null removes the default limit and returns all objects
    $computers = @(Get-ADComputer -Server $dc -SearchBase $searchBase -LDAPFilter $ldap -Properties $props -ResultPageSize 500 -ResultSetSize $null)

    Write-Log -LogFile $logFile -Level INFO -Message "$($computers.Count) objects returned by AD before exclusion filters."

    # Filters objects whose DN is contained in any excluded OU (or its sub-OUs).
    # Uses .Contains() to cover nested sub-OUs at any level:
    #   DN "CN=SRV01,OU=SQL,OU=Servers,DC=corp,DC=com" contains "ou=servers,dc=corp,dc=com"
    #   DN "CN=SRV02,OU=Servers,DC=corp,DC=com"        contains "ou=servers,dc=corp,dc=com"
    # The prefixed comma ensures no false positive from partial OU name:
    #   "ou=servers2,..." would not be blocked by an exclusion of "ou=servers,..."
    if ($excludedOUNorm.Count -gt 0) {
        $beforeFilter = $computers.Count
        $computers = @($computers | Where-Object {
            $dnLower = $_.DistinguishedName.ToLower()
            $blocked = $false
            foreach ($ouNorm in $excludedOUNorm) {
                if ($dnLower.Contains(",$ouNorm")) {
                    $blocked = $true; break
                }
            }
            -not $blocked
        })
        $filteredByOU = $beforeFilter - $computers.Count
        Write-Log -LogFile $logFile -Level INFO -Message "$filteredByOU object(s) excluded by -ExcludeServerOU. $($computers.Count) remaining for analysis."
    }

    # ------------------------------------------------------------------
    # EXECUTION AND REMEDIATION CONTROLS
    # ------------------------------------------------------------------

    $firstInventory = $true; $firstActions = $true; $firstFailed = $true
    $foundWorkstations = 0; $skippedServers = 0; $inactiveCount = 0; $failedCount = 0; $idx = 0
    $total = $computers.Count

    # Uses local bool variable to avoid reassigning the original [switch] parameter
    $doRemediate = $Remediate.IsPresent

    # Prompts for target OU BEFORE the loop to avoid interrupting processing per iteration
    if ($doRemediate -and [string]::IsNullOrWhiteSpace($TargetOU)) {
        $TargetOU = Read-Host 'Enter the target OU (DN)'
        if ([string]::IsNullOrWhiteSpace($TargetOU)) {
            Write-Log -LogFile $logFile -Level ERROR -Message 'Target OU is empty. Remediation cancelled.'
            $doRemediate = $false
        }
    }

    # Validates whether the target OU exists in AD before starting the loop.
    # Prevents an invalid OU from only being discovered on the first Move-ADObject attempt,
    # which would result in a partial failure (account disabled but not moved) for each object.
    if ($doRemediate) {
        try {
            Get-ADOrganizationalUnit -Identity $TargetOU -Server $dc -ErrorAction Stop | Out-Null
            Write-Log -LogFile $logFile -Level INFO -Message "Target OU validated: $TargetOU"
        } catch {
            Write-Log -LogFile $logFile -Level ERROR -Message "Target OU invalid or inaccessible: $TargetOU | $($_.Exception.Message)"
            Write-Log -LogFile $logFile -Level ERROR -Message 'Remediation cancelled. No changes were made to AD.'
            $doRemediate = $false
        }
    }

    # ------------------------------------------------------------------
    # MAIN PROCESSING
    # ------------------------------------------------------------------

    foreach ($c in $computers) {
        $idx++
        Write-Log -LogFile $logFile -Level INFO -Message "Processing $idx of $total : $($c.Name)"

        # Additional safety: ignores objects that the LDAP filter may not have excluded
        if ($c.OperatingSystem -and $c.OperatingSystem -match 'Server') { $skippedServers++; continue }

        $foundWorkstations++
        $lastLogon = Get-LastLogonDate -ComputerObj $c

        # Inactive if never logged on ($null) or if last logon was before the cutoff
        $isInactive = ($null -eq $lastLogon) -or ($lastLogon -lt $cutoff)

        if ($isInactive) {
            $inactiveCount++

            # Inventory record — selected fields for the report
            $row = [pscustomobject]@{
                Domain               = $Domain
                Name                 = $c.Name
                OperatingSystem      = $c.OperatingSystem
                LastLogonDate        = $lastLogon
                InactiveDaysApprox   = if ($lastLogon) {[int]((New-TimeSpan -Start $lastLogon -End (Get-Date)).TotalDays)} else {$null}
                AccountEnabled       = $c.Enabled
                AlreadyInTargetOU    = if ([string]::IsNullOrWhiteSpace($TargetOU)) { $null } else { $c.DistinguishedName.ToLower().Contains(",$($TargetOU.ToLower())") }
                DistinguishedName    = $c.DistinguishedName
            }

            # Incremental export: writes header only on the first row,
            # then only data to avoid duplicate headers in the CSV
            $csvText = $row | ConvertTo-Csv -NoTypeInformation
            if ($firstInventory) {
                $csvText | Out-File -FilePath $csvInventory -Encoding $CsvEncoding -Force
                $firstInventory = $false
            } else {
                $csvText | Select-Object -Skip 1 | Out-File -FilePath $csvInventory -Encoding $CsvEncoding -Append
            }

            # ----------------------------------------------------------
            # REMEDIATION (only with -Remediate)
            # ----------------------------------------------------------
            # ShouldProcess enables native -WhatIf and -Confirm support in PowerShell
            if ($doRemediate -and $PSCmdlet.ShouldProcess($c.DistinguishedName, "Disable and move to $TargetOU")) {
                $disabled = $false; $moved = $false; $err = $null

                try {
                    # Step 1: disable the account in AD
                    Disable-ADAccount -Identity $c.DistinguishedName -Confirm:$false
                    $disabled = $true

                    # Step 2: move the object to the quarantine OU
                    # The original DN is still valid after Disable-ADAccount (only the password/flag changes)
                    Move-ADObject -Identity $c.DistinguishedName -TargetPath $TargetOU -Confirm:$false
                    $moved = $true

                    Write-Log -LogFile $logFile -Level OK -Message "Remediated: $($c.Name)"
                } catch {
                    # Partial failure: records the error but continues the loop for the remaining objects
                    $err = $_.Exception.Message
                    Write-Log -LogFile $logFile -Level ERROR -Message "Failure: $($c.Name) | $err"
                }

                # Audit trail: records the result of each remediation attempt
                # Includes Domain and DistinguishedName for traceability in multi-domain environments
                $actionObj = [pscustomobject]@{
                    Domain            = $Domain
                    Name              = $c.Name
                    DistinguishedName = $c.DistinguishedName
                    Disabled          = $disabled
                    Moved             = $moved
                    TargetOU          = $TargetOU
                    Error             = $err
                    Timestamp         = (Get-Date)
                }
                $actCsv = $actionObj | ConvertTo-Csv -NoTypeInformation
                if ($firstActions) {
                    $actCsv | Out-File -FilePath $csvActions -Encoding $CsvEncoding -Force; $firstActions = $false
                } else {
                    $actCsv | Select-Object -Skip 1 | Out-File -FilePath $csvActions -Encoding $CsvEncoding -Append
                }

                # Failure CSV: records only computers where at least one step failed
                if (-not $disabled -or -not $moved) {
                    $failedCount++
                    $failCsv = $actionObj | ConvertTo-Csv -NoTypeInformation
                    if ($firstFailed) {
                        $failCsv | Out-File -FilePath $csvFailed -Encoding $CsvEncoding -Force; $firstFailed = $false
                    } else {
                        $failCsv | Select-Object -Skip 1 | Out-File -FilePath $csvFailed -Encoding $CsvEncoding -Append
                    }
                }
            }
        }
    }

    $summary = "Completed | Inactive: $inactiveCount of $foundWorkstations workstations analyzed."
    if ($doRemediate) {
        $summary += " | Remediation failures: $failedCount"
        if ($failedCount -gt 0) {
            $summary += " | Check: $csvFailed"
        }
    }
    Write-Log -LogFile $logFile -Level OK -Message $summary
}

