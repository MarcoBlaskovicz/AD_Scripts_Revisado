function Initialize-Folders {
<#
.SYNOPSIS
    Creates the folder structure to store logs and CSVs for the execution.
.DESCRIPTION
    Ensures the root folder exists and creates a unique subfolder per execution,
    named with the domain and current timestamp (yyyyMMdd_HHmmss).
    Returns the full path of the created subfolder.
.PARAMETER Root
    Root folder path. Created automatically if it does not exist.
.PARAMETER DomainTag
    Domain identifier (dots replaced by underscores) used
    to compose the execution subfolder name.
#>
    param(
        [string]$Root = 'C:\Temp\LogScripts\AD_Inactive_Workstations',
        [string]$DomainTag
    )
    if (-not (Test-Path $Root)) { New-Item -Path $Root -ItemType Directory -Force | Out-Null }
    $runTag = "{0}_{1}" -f $DomainTag, (Get-Date -Format 'yyyyMMdd_HHmmss')
    $runDir = Join-Path $Root $runTag
    New-Item -Path $runDir -ItemType Directory -Force | Out-Null
    return $runDir
}

function Write-Log {
<#
.SYNOPSIS
    Writes a log line to the console and file simultaneously.
.DESCRIPTION
    Formats the message with timestamp and level (INFO/WARN/ERROR/OK),
    displays to the console via Write-Host and appends to the execution log file.
.PARAMETER Message
    Text of the message to be recorded.
.PARAMETER LogFile
    Full path of the destination log file (.log).
.PARAMETER Level
    Message severity. Values: INFO (default), WARN, ERROR, OK.
#>
    param(
        [Parameter(Mandatory)] [string]$Message,
        [Parameter(Mandatory)] [string]$LogFile,
        [ValidateSet('INFO','WARN','ERROR','OK')] [string]$Level = 'INFO'
    )
    $line = "{0} [{1}] {2}" -f (Get-Date -Format 'yyyy-MM-dd HH:mm:ss'), $Level, $Message
    Write-Host $line
    Add-Content -Path $LogFile -Value $line
}

function Get-LastLogonDate {
<#
.SYNOPSIS
    Returns the last logon date of an AD computer object.
.DESCRIPTION
    Attempts to obtain the last logon date using two attributes, in order of
    preference:

      1. LastLogonDate  : already converted to [DateTime], replicated between DCs
                          (up to 14 days latency). Used when available.
      2. LastLogonTimestamp : raw value in Windows FileTime format (Int64).
                              Converted via [DateTime]::FromFileTime().
                              Used as fallback when LastLogonDate is absent.

    Returns $null if neither attribute is available (computer has never
    logged on or attributes have not been replicated).
.PARAMETER ComputerObj
    ADComputer object returned by Get-ADComputer with the
    LastLogonDate and LastLogonTimestamp properties already loaded.
#>
    param($ComputerObj)

    # Priority 1: LastLogonDate (already [DateTime], replicated between DCs)
    if ($ComputerObj.LastLogonDate) { return $ComputerObj.LastLogonDate }

    # Priority 2: LastLogonTimestamp (raw FileTime, not replicated — local value of the queried DC)
    if ($null -ne $ComputerObj.LastLogonTimestamp -and $ComputerObj.LastLogonTimestamp -ne 0) {
        try { return [DateTime]::FromFileTime([int64]$ComputerObj.LastLogonTimestamp) } catch { return $null }
    }

    # No logon data available
    return $null
}

# ==============================================================================
# ROLLBACK FUNCTION
# ==============================================================================

function Invoke-Rollback {
<#
.SYNOPSIS
    Undoes a previous remediation by reading the actions CSV generated by -Remediate.
.DESCRIPTION
    For each row in the CSV where Disabled=True:
      1. Re-enables the account in AD (Enable-ADAccount).
      2. Extracts the original OU from the DistinguishedName recorded in the CSV (removes the
         initial CN= component to get the parent OU path).
      3. Checks if the original OU still exists. If so, moves the object back.
         If not, keeps the account re-enabled and logs a warning — the account is NOT
         left disabled due to a move failure.
    Rows where Disabled=False are ignored (the disable already failed
    in the original remediation, so there is nothing to undo).
    Supports -WhatIf and -Confirm natively via CmdletBinding.
.PARAMETER CsvPath
    Path of the actions CSV generated by a previous run with -Remediate.
.PARAMETER Dc
    Hostname of the DC to be used in AD operations.
.PARAMETER LogFile
    Path of the log file for this rollback execution.
.PARAMETER CsvRollbackActions
    Path of the rollback actions audit CSV.
.PARAMETER CsvRollbackFailed
    Path of the rollback failures exclusive CSV.
.PARAMETER CsvEncoding
    Encoding for writing output CSVs.
.PARAMETER PSCmdlet
    PSCmdlet object from the calling function, required to propagate ShouldProcess.
#>
    [CmdletBinding(SupportsShouldProcess=$true, ConfirmImpact='High')]
    param(
        [Parameter(Mandatory)] [string]$CsvPath,
        [Parameter(Mandatory)] [string]$Dc,
        [Parameter(Mandatory)] [string]$LogFile,
        [Parameter(Mandatory)] [string]$CsvRollbackActions,
        [Parameter(Mandatory)] [string]$CsvRollbackFailed,
        [Parameter(Mandatory)] [string]$CsvEncoding,
        [Parameter(Mandatory)] $CallerCmdlet
    )

    # Load the actions CSV from the original remediation
    $actions = @(Import-Csv -Path $CsvPath -Encoding $CsvEncoding)
    $total   = $actions.Count
    Write-Log -LogFile $LogFile -Level INFO -Message "Rollback | $total entries read from: $CsvPath"

    # Filter only entries where the disable was effectively performed
    $toProcess = @($actions | Where-Object { $_.Disabled -eq 'True' })
    $skipped   = $total - $toProcess.Count
    Write-Log -LogFile $LogFile -Level INFO -Message "Rollback | $($toProcess.Count) accounts to re-enable | $skipped ignored (Disabled=False)"

    $firstRbActions = $true; $firstRbFailed = $true
    $idx = 0; $successCount = 0; $failedCount = 0

    foreach ($entry in $toProcess) {
        $idx++
        $dn   = $entry.DistinguishedName
        $name = $entry.Name
        Write-Log -LogFile $LogFile -Level INFO -Message "Rollback $idx of $($toProcess.Count) : $name"

        # Extracts the original OU: removes the first component (CN=<name>,) from the original DN.
        # Example: "CN=PC01,OU=IT,DC=corp,DC=com" -> "OU=IT,DC=corp,DC=com"
        $originalOU = $dn -replace '^CN=[^,]+,', ''

        if (-not $CallerCmdlet.ShouldProcess($dn, "Re-enable and move back to $originalOU")) { continue }

        $reenabled   = $false
        $movedBack   = $false
        $ouMissing   = $false
        $err         = $null

        # The object was moved to the quarantine OU; its current DN uses the same CN
        # but with the TargetOU path recorded in the CSV
        $currentDN = "CN=$name,$($entry.TargetOU)"

        try {
            # Step 1: re-enable the account using the current DN (in the quarantine OU)
            Enable-ADAccount -Identity $currentDN -Confirm:$false
            $reenabled = $true
            Write-Log -LogFile $LogFile -Level OK -Message "Re-enabled: $name"
        } catch {
            $err = $_.Exception.Message
            Write-Log -LogFile $LogFile -Level ERROR -Message "Failed to re-enable $name | $err"
        }

        # Step 2: attempts to move back to the original OU (only if re-enable succeeded)
        if ($reenabled) {
            try {
                Get-ADOrganizationalUnit -Identity $originalOU -Server $Dc -ErrorAction Stop | Out-Null
            } catch {
                # Original OU no longer exists — logs a warning but keeps account active
                $ouMissing = $true
                Write-Log -LogFile $LogFile -Level WARN -Message "Original OU not found for $name ($originalOU). Account re-enabled but not moved. Manual relocation required."
            }

            if (-not $ouMissing) {
                try {
                    Move-ADObject -Identity $currentDN -TargetPath $originalOU -Confirm:$false
                    $movedBack = $true
                    Write-Log -LogFile $LogFile -Level OK -Message "Moved back: $name -> $originalOU"
                } catch {
                    $err = $_.Exception.Message
                    Write-Log -LogFile $LogFile -Level ERROR -Message "Failed to move $name back | $err"
                }
            }
        }

        # Determines consolidated result
        $rollbackOk = $reenabled -and ($movedBack -or $ouMissing)
        if ($rollbackOk) { $successCount++ } else { $failedCount++ }

        # Rollback audit
        $rbObj = [pscustomobject]@{
            Domain            = $entry.Domain
            Name              = $name
            DistinguishedName = $dn
            OriginalOU        = $originalOU
            Reenabled         = $reenabled
            MovedBack         = $movedBack
            OUMissing         = $ouMissing
            Error             = $err
            Timestamp         = (Get-Date)
        }
        $rbCsv = $rbObj | ConvertTo-Csv -NoTypeInformation
        if ($firstRbActions) {
            $rbCsv | Out-File -FilePath $CsvRollbackActions -Encoding $CsvEncoding -Force
            $firstRbActions = $false
        } else {
            $rbCsv | Select-Object -Skip 1 | Out-File -FilePath $CsvRollbackActions -Encoding $CsvEncoding -Append
        }

        # Failure CSV: only when re-enable failed (missing OU is not a failure)
        if (-not $reenabled) {
            $failCsv = $rbObj | ConvertTo-Csv -NoTypeInformation
            if ($firstRbFailed) {
                $failCsv | Out-File -FilePath $CsvRollbackFailed -Encoding $CsvEncoding -Force
                $firstRbFailed = $false
            } else {
                $failCsv | Select-Object -Skip 1 | Out-File -FilePath $CsvRollbackFailed -Encoding $CsvEncoding -Append
            }
        }
    }

    $summary = "Rollback completed | Success: $successCount | Failures: $failedCount | Ignored: $skipped"
    if ($failedCount -gt 0) { $summary += " | Check: $CsvRollbackFailed" }
    Write-Log -LogFile $LogFile -Level OK -Message $summary
}

