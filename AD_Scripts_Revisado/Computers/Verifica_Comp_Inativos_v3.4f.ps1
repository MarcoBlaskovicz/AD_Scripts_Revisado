# param block at the script scope: required for PowerShell to recognize
# command-line arguments and make them available in $PSBoundParameters,
# allowing automatic forwarding to Invoke-InactiveWorkstations via splatting.
[CmdletBinding(SupportsShouldProcess=$true, ConfirmImpact='High')]
param(
    [Parameter(Position=0, Mandatory=$false)] [string]$Domain,
    [int]$Days = 60,
    [switch]$Remediate,
    [string]$TargetOU,
    [switch]$Rollback,
    [string]$RollbackCsv,
    [string[]]$ExcludeServerOU = @(),
    [string]$Root = 'C:\Temp\LogScripts\AD_Inactive_Workstations',
    [ValidateSet('utf8','unicode','ascii')] [string]$CsvEncoding = 'utf8'
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

